def main():
    # Use a for loop to count down from 10 to 1
    for i in range(10, 0, -1):
        print(i, end=" ")  # Print each number followed by a space
    
    # Print Liftoff! after the countdown
    print("Liftoff!")

# This ensures the main function runs when the script is executed
if __name__ == '__main__':
    main()
